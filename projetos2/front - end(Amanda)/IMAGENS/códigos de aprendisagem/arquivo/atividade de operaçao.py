valor1 = int(input("digite o valor 1:")) 
valor2 = int(input("digite o valor 2:")) 
operação =(input("qual operaçãao deseja realizar?(+,-,*,/)")) 

if operação == '+':
    print(f'o resultado da soma é:{valor1+valor2}')
if operação == '*':
    print(f'o resultado da soma é:{valor1*valor2}')
if operação == '/':
    print(f'o resultado da soma é:{valor1/valor2}')
if operação == '-':
    print(f'o resultado da soma é:{valor1-valor2}')



    
    
    
    






