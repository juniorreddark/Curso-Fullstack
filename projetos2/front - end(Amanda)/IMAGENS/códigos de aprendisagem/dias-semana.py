dia_da_semana = input("Digite o dia da semana: ")

if dia_da_semana == "segunda-feira":
    print("Hora de começar a semana com força!")
elif dia_da_semana == "terça-feira":
    print("Ufa, já é terça-feira?")
elif dia_da_semana == "quarta-feira":
    print("Corcunda no lombo? Quarta-feira chegou!")
elif dia_da_semana == "quinta-feira":
    print("Quase chegando no fim de semana!")
elif dia_da_semana == "sexta-feira":
    print("Sextou! Hora de relaxar!")
elif dia_da_semana == "sábado":
    print("Sábado é dia de curtir!")
elif dia_da_semana == "domingo":  
    print("ho gloria que chegou esse dia")
else:
    print("Dia inválido. Tente novamente.")
